export type RewardTranslationKey = keyof typeof rewardTranslations.en;

export const getRewardTranslation = (
  locale: string,
  key: RewardTranslationKey,
  params?: Record<string, string | number>
): string => {
  const translations = locale === 'th' ? rewardTranslations.th : rewardTranslations.en;
  let text = translations[key] || rewardTranslations.en[key];
  
  if (params) {
    Object.keys(params).forEach((param) => {
      text = text.replace(new RegExp(`{{${param}}}`, 'g'), String(params[param]));
    });
  }
  
  return text;
};
