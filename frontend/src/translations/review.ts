export type ReviewTranslationKey = keyof typeof reviewTranslations.en;

export const getReviewTranslation = (
  locale: string,
  key: ReviewTranslationKey,
  params?: Record<string, string | number>
): string => {
  const translations = locale === 'th' ? reviewTranslations.th : reviewTranslations.en;
  let text = translations[key] || reviewTranslations.en[key];
  
  if (params) {
    Object.keys(params).forEach((param) => {
      text = text.replace(new RegExp(`{{${param}}}`, 'g'), String(params[param]));
    });
  }
  
  return text;
};
